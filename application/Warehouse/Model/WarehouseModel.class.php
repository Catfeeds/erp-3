    public function add_quantity($where,$quantity=0){
        $result = D('Common/WarehouseProduct')->where($where)->find();
        if($result){
            $set_qty = $result['quantity']+$quantity;
            D('Common/WarehouseProduct')->where($where)->save(array('quantity'=>$set_qty));
        }
    }

    /**
     * 获取所有仓库
     * @param bool $warehouse_id
     * @return array|mixed
     *
     */
    public function all_warehouse($warehouse_id = false) {
        $cache = F('getAllWarehouseList');
        if ($cache) {
            $warehouse_list = unserialize($cache);
        } else {
            $warehouse_list = array();
            $warehouse =  D("Common/Warehouse")->field('id_warehouse,title')->cache(true, 600)->select();
            if ($warehouse) {
                foreach ($warehouse as $item) {
                    $warehouse_list[$item['id_warehouse']] = $item['title'];
                }
            }
            F('getAllWarehouseList', serialize($warehouse_list));
        }
        $return = $warehouse_id ? $warehouse_list[$warehouse_id] : $warehouse_list;
        return $return;
    }
